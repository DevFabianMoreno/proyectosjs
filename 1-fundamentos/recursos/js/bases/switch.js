const dia = "5";

switch(dia){
    case 0:
        console.log("Domingo");
        break;
    case 1:
        console.log("Lunes");
        break;
    case 2:
        console.log("Martes");
        break;
    case "5": 
        console.log("Es 5 pero en string");
        break;
    default:
        console.log("No es ni Domingo, Lunes ni Martes") 
}
